void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance == TIM3)
  {
    HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
    
    if(TIM2->CNT>=32767)
    {
        speed_temp = TIM2->CNT - 32767;
    }
    else if(TIM2->CNT<32767)
    {
        speed_temp = 32767 - TIM2->CNT;
    }

    int speed_temp_2 = speed_temp;
    int i = 1;
    while(speed_temp_2/10)
    {
        i++;
        speed_temp_2 /= 10;
    }
    
    int j = 0;
    uint8_t speed[6];
    speed_temp_2 = speed_temp;
    for(j=5; j>i-1; j--)
    {
        speed[j] = ' ';
    }
    for(j=i-1; j>-1; j--)
    {
        speed[j] = speed_temp_2%10 + '0';
        speed_temp_2 /= 10;
    }
    HAL_UART_Transmit(&huart1, (uint8_t *)speed, 6, 100);
    
    TIM2->CNT = 65535/2;
    
    speed_PID(expectation);
    
    send_wave(speed_temp, expectation, 0, 0);
  }
}

void speed_PID(int expectation)
{
    //007
    double Kp=700.0;
    double Kd=7.0;
    double Ki=70.0;
    
    double p_out=0.0, d_out=0.0, i_out=0.0;
    
    this_error = expectation - speed_temp;
    total_error += this_error;
    
    p_out = Kp * this_error;
    d_out = Kd * (this_error - last_error);
    i_out = Ki * total_error;
    
    TIM4->CCR2 = (int)(p_out + d_out + i_out);
    
    last_error = this_error;
}

void send_wave(float arg1,float arg2,float arg3,float arg4)
{
    s[20]='\r';
    s[21]='\n';
    memcpy(s+4,&arg1,sizeof(arg1));
    memcpy(s+8,&arg2,sizeof(arg1));
    memcpy(s+12,&arg3,sizeof(arg1));
    memcpy(s+16,&arg4,sizeof(arg1));
    HAL_UART_Transmit(&huart1,(uint8_t *)s,sizeof(s),2000);
}
